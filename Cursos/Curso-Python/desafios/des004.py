# Le oque foi digitado e mostra todas as informações sobre..
p = input('Digite algo:')
print("É Númerico:", p.isnumeric())
print("É Alpha:", p.isalpha())
print("É Alpha-númerico:", p.isalnum())
print("Está Maiusculo:", p.isupper())
print("Está Minusculo:", p.islower())
print("É um espaço:", p.isspace())